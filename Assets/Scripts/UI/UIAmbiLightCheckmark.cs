using System.Collections;
using System.Collections.Generic;
using UnityEngine;
using UnityEngine.UI;

public class UIAmbiLightCheckmark : MonoBehaviour
{
	Globals.AmbiLight ambilight;
	Image thisImage;

	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

	void Awake()
	{
		AmbiLightButtonController abc = GetComponentInParent<AmbiLightButtonController>();
		ambilight = abc.ambilight;
		thisImage = GetComponent<Image>();
		Messenger.AddListener(MessengerEventsEnum.CHANGE_AMBILIGHT, OnChange);
	}

	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

	void Start()
	{
		OnChange();
	}

	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

	void OnDestroy()
	{
		Messenger.RemoveListener(MessengerEventsEnum.CHANGE_AMBILIGHT, OnChange);
	}

	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

	void OnChange()
	{
		thisImage.enabled = (Globals.instance.currentAmbiLight == ambilight) ? true : false;
	}
}