using System.Collections;
using System.Collections.Generic;
using UnityEngine;
using UnityEngine.UI;

public class UpholsteryNameController : MonoBehaviour
{
	Text text_name;

	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

	void Awake()
	{
		Messenger.AddListener(MessengerEventsEnum.CHANGE_INT_UPHOLSTERY, OnUpholsteryChanged);
		text_name = GetComponent<Text>();
	}

	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

	void Start()
	{
		OnUpholsteryChanged();
	}

	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

	void OnDestroy()
	{
		Messenger.RemoveListener(MessengerEventsEnum.CHANGE_INT_UPHOLSTERY, OnUpholsteryChanged);
	}

	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

	void OnUpholsteryChanged()
	{
		string s = Globals.instance.currentUpholstery.ToString();
		s = s.Substring(s.IndexOf("_")+1);
		s = s.Replace("_", " ");
		text_name.text = s;
	}
}