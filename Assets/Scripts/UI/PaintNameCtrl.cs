using System.Collections;
using System.Collections.Generic;
using UnityEngine;
using UnityEngine.UI;

public class PaintNameCtrl : MonoBehaviour
{
	Text text_name;

	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

	void Awake()
	{
		Messenger.AddListener(MessengerEventsEnum.CHANGE_EXT_BODY_COLOR, OnColorChanged);
		text_name = GetComponent<Text>();
	}

	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

	void Start()
	{
		OnColorChanged();
	}

	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

	void OnDestroy()
	{
		Messenger.RemoveListener(MessengerEventsEnum.CHANGE_EXT_BODY_COLOR, OnColorChanged);
	}

	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	
	void OnColorChanged()
	{
		string s = Globals.instance.currentPaintBody.ToString();
		s = s.Replace("METAL_", "");
		s = s.Replace("_", " ");
		text_name.text = s;
	}
}