using System.Collections;
using System.Collections.Generic;
using UnityEngine;
using UnityEngine.UI;
using System;

public class TiresNameCtrl : MonoBehaviour
{
	Text text_name;

	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

	void Awake()
	{
		Messenger.AddListener(MessengerEventsEnum.CHANGE_EXT_TIRES, OnTiresChanged);
		text_name = GetComponent<Text>();
	}

	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

	void Start()
	{
		OnTiresChanged();
	}

	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

	void OnDestroy()
	{
		Messenger.RemoveListener(MessengerEventsEnum.CHANGE_EXT_TIRES, OnTiresChanged);
	}

	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

	void OnTiresChanged()
	{
		string s = Globals.instance.currentTires.ToString();
		s = s.Substring(s.IndexOf("_")+1);
		s = s.Replace("_", " ");
		text_name.text = s;
	}
}