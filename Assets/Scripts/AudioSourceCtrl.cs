using UnityEngine;

public class AudioSourceCtrl : MonoBehaviour
{
	private AudioSource audioSource;

	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

	private void Awake ()
	{
		audioSource = GetComponent<AudioSource>();
	}

	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

	public bool PlayBool()
	{
		if(audioSource != null && audioSource.isPlaying){
			return false;
		}
		audioSource.Play();
		return true;
	}

	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

	public void Play()
	{
		if(audioSource != null && !audioSource.isPlaying){
			audioSource.Play();
		}
	}
}
